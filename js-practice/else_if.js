// Program 1: Grade system
let marks = 85;
if (marks >= 90) {
  console.log("Program 1: Grade A");
} else if (marks >= 75) {
  console.log("Program 1: Grade B");
} else if (marks >= 60) {
  console.log("Program 1: Grade C");
} else {
  console.log("Program 1: Fail");
}

// Program 2: Number type
let num = 0;
if (num > 0) {
  console.log("Program 2: Positive number");
} else if (num < 0) {
  console.log("Program 2: Negative number");
} else {
  console.log("Program 2: Zero");
}

// Program 3: Age category
let age = 65;
if (age < 13) {
  console.log("Program 3: Child");
} else if (age < 60) {
  console.log("Program 3: Adult");
} else {
  console.log("Program 3: Senior citizen");
}

// Program 4: Traffic signal
let signal = "yellow";
if (signal === "red") {
  console.log("Program 4: Stop");
} else if (signal === "yellow") {
  console.log("Program 4: Ready");
} else if (signal === "green") {
  console.log("Program 4: Go");
} else {
  console.log("Program 4: Invalid signal");
}

